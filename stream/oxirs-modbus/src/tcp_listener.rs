    /// Always returns a fixed response with the given data bytes.
    struct EchoHandler {
        function_code: u8,
        response_data: Vec<u8>,
    }

    impl RequestHandler for EchoHandler {
        fn handle(&self, frame: &ModbusFrame) -> DispatchResult {
            DispatchResult::Handled(ModbusTcpListener::build_response(
                frame,
                self.response_data.clone(),
            ))
        }
        fn supports(&self, fc: u8) -> bool {
            fc == self.function_code
        }
    }

    /// Always returns a Modbus error.
    struct ErrorHandler {
        function_code: u8,
        error_code: u8,
    }

    impl RequestHandler for ErrorHandler {
        fn handle(&self, _frame: &ModbusFrame) -> DispatchResult {
            DispatchResult::Error(format!("error_code={}", self.error_code))
        }
        fn supports(&self, fc: u8) -> bool {
            fc == self.function_code
        }
    }

    /// Handler that panics if called — used to test "first match wins".
    struct NeverCalledHandler;

    impl RequestHandler for NeverCalledHandler {
        fn handle(&self, _frame: &ModbusFrame) -> DispatchResult {
            panic!("NeverCalledHandler should not be invoked");
        }
        fn supports(&self, _fc: u8) -> bool {
            true // supports everything — but should be shadowed by earlier handler
        }
    }

