    // 43. FC 0x05 (Write Single Coil) can be handled
    #[test]
    fn test_write_single_coil_handled() {
        let mut listener = default_listener();
        listener.register_handler(Box::new(EchoHandler {
            function_code: 0x05,
            response_data: vec![0x00, 0x01, 0xFF, 0x00],
        }));
        let f = ModbusFrame::new(1, 1, 0x05, vec![0x00, 0x01, 0xFF, 0x00]);
        listener.enqueue(f).expect("enqueue");
        let resp = listener.process_next().expect("response");
        assert!(!resp.is_error);
        assert_eq!(resp.function_code, 0x05);
    }

    // 44. Unhandled response contains data with exception code
    #[test]
    fn test_unhandled_response_data() {
        let mut listener = default_listener();
        let f = ModbusFrame::new(1, 1, 0x04, vec![]);
        listener.enqueue(f).expect("enqueue");
        let resp = listener.process_next().expect("response");
        assert_eq!(resp.data, vec![0x01]); // illegal function
    }

    // 45. Multiple handlers registered, each for unique fc
    #[test]
    fn test_multiple_unique_handlers() {
        let mut listener = default_listener();
        for fc in [0x01u8, 0x02, 0x03, 0x04, 0x05] {
            listener.register_handler(Box::new(EchoHandler {
                function_code: fc,
                response_data: vec![fc],
            }));
        }
        for fc in [0x01u8, 0x02, 0x03, 0x04, 0x05] {
            listener.enqueue(frame(fc)).expect("enqueue");
        }
        let responses = listener.process_all();
        for (i, fc) in [0x01u8, 0x02, 0x03, 0x04, 0x05].iter().enumerate() {
            assert_eq!(responses[i].data, vec![*fc]);
        }
    }
