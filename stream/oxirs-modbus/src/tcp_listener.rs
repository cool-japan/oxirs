//! Modbus TCP frame listener and dispatcher (pure Rust, in-memory simulation).
//!
//! This module provides an in-memory Modbus TCP frame dispatcher that accepts
//! frames via a queue, routes them through registered handlers, and accumulates
//! responses.  No real network I/O is performed, making it ideal for unit tests
//! and simulation environments.

use std::collections::VecDeque;

// ─────────────────────────────────────────────────────────────────────────────
// Public types
// ─────────────────────────────────────────────────────────────────────────────

/// A Modbus ADU (Application Data Unit) received over TCP (MBAP + PDU).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ModbusFrame {
    /// Transaction identifier from MBAP header
    pub transaction_id: u16,
    /// Unit (slave) identifier
    pub unit_id: u8,
    /// Modbus function code
    pub function_code: u8,
    /// PDU data (everything after the function code byte)
    pub data: Vec<u8>,
}

impl ModbusFrame {
    /// Construct a new Modbus frame.
    pub fn new(transaction_id: u16, unit_id: u8, function_code: u8, data: Vec<u8>) -> Self {
        Self {
            transaction_id,
            unit_id,
            function_code,
            data,
        }
    }
}

/// A Modbus response generated by a handler.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ModbusResponse {
    pub transaction_id: u16,
    pub unit_id: u8,
    /// Mirror of the request function code (or function code | 0x80 for errors)
    pub function_code: u8,
    pub data: Vec<u8>,
    /// `true` when this is an exception response
    pub is_error: bool,
}

/// Outcome of dispatching a frame to the registered handlers.
#[derive(Debug)]
pub enum DispatchResult {
    /// A handler processed the frame and produced a response.
    Handled(ModbusResponse),
    /// No handler claimed the frame.
    Unhandled,
    /// A handler encountered an error.
    Error(String),
}

/// Trait implemented by concrete Modbus request handlers.
pub trait RequestHandler: Send + Sync {
    /// Process a frame and return the dispatch outcome.
    fn handle(&self, frame: &ModbusFrame) -> DispatchResult;
    /// Return `true` if this handler can process `function_code`.
    fn supports(&self, function_code: u8) -> bool;
}

/// Configuration for the TCP listener simulation.
#[derive(Debug, Clone)]
pub struct TcpListenerConfig {
    /// Maximum number of concurrent connections (informational)
    pub max_connections: usize,
    /// Request timeout in milliseconds (informational)
    pub request_timeout_ms: u64,
    /// Maximum number of frames that can be enqueued
    pub max_queue_size: usize,
}

impl Default for TcpListenerConfig {
    fn default() -> Self {
        Self {
            max_connections: 10,
            request_timeout_ms: 1_000,
            max_queue_size: 256,
        }
    }
}

/// Errors produced by the listener.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ListenerError {
    /// Pending queue is at capacity.
    QueueFull,
    /// The frame failed basic validation.
    InvalidFrame(String),
}

impl std::fmt::Display for ListenerError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::QueueFull => write!(f, "listener queue is full"),
            Self::InvalidFrame(msg) => write!(f, "invalid frame: {msg}"),
        }
    }
}

impl std::error::Error for ListenerError {}

/// In-memory Modbus TCP listener / dispatcher.
pub struct ModbusTcpListener {
    config: TcpListenerConfig,
    handlers: Vec<Box<dyn RequestHandler>>,
    pending: VecDeque<ModbusFrame>,
    processed: usize,
    errors: usize,
}

impl ModbusTcpListener {
    /// Create a new listener with the given configuration.
    pub fn new(config: TcpListenerConfig) -> Self {
        Self {
            config,
            handlers: Vec::new(),
            pending: VecDeque::new(),
            processed: 0,
            errors: 0,
        }
    }

    /// Register a request handler.  Handlers are tried in registration order;
    /// the first one that `supports()` the function code handles the frame.
    pub fn register_handler(&mut self, handler: Box<dyn RequestHandler>) {
        self.handlers.push(handler);
    }

    /// Enqueue a frame for processing.
    pub fn enqueue(&mut self, frame: ModbusFrame) -> Result<(), ListenerError> {
        if self.pending.len() >= self.config.max_queue_size {
            return Err(ListenerError::QueueFull);
        }
        // Basic validation: function code must be non-zero
        if frame.function_code == 0 {
            return Err(ListenerError::InvalidFrame(
                "function_code must not be 0".to_string(),
            ));
        }
        self.pending.push_back(frame);
        Ok(())
    }

    /// Process the next frame in the queue and return the response, if any.
    pub fn process_next(&mut self) -> Option<ModbusResponse> {
        let frame = self.pending.pop_front()?;
        let result = self.dispatch(&frame);
        match result {
            DispatchResult::Handled(resp) => {
                self.processed += 1;
                Some(resp)
            }
            DispatchResult::Error(_msg) => {
                self.errors += 1;
                self.processed += 1;
                // Return a generic server error response (exception code 04)
                Some(Self::build_error_response(&frame, 0x04))
            }
            DispatchResult::Unhandled => {
                self.errors += 1;
                self.processed += 1;
                // Return illegal function exception (01)
                Some(Self::build_error_response(&frame, 0x01))
            }
        }
    }

    /// Process all queued frames and return all responses.
    pub fn process_all(&mut self) -> Vec<ModbusResponse> {
        let mut responses = Vec::new();
        while let Some(resp) = self.process_next() {
            responses.push(resp);
        }
        responses
    }

    /// Number of frames waiting to be processed.
    pub fn pending_count(&self) -> usize {
        self.pending.len()
    }

    /// Total frames that have been processed (including errors).
    pub fn processed_count(&self) -> usize {
        self.processed
    }

    /// Total frames that resulted in an error or were unhandled.
    pub fn error_count(&self) -> usize {
        self.errors
    }

    /// Number of registered handlers.
    pub fn handler_count(&self) -> usize {
        self.handlers.len()
    }

    /// Build a successful response for `req` with the provided data payload.
    pub fn build_response(req: &ModbusFrame, data: Vec<u8>) -> ModbusResponse {
        ModbusResponse {
            transaction_id: req.transaction_id,
            unit_id: req.unit_id,
            function_code: req.function_code,
            data,
            is_error: false,
        }
    }

    /// Build an exception response for `req` with the given Modbus exception code.
    pub fn build_error_response(req: &ModbusFrame, error_code: u8) -> ModbusResponse {
        ModbusResponse {
            transaction_id: req.transaction_id,
            unit_id: req.unit_id,
            // Exception responses have the high bit set on the function code
            function_code: req.function_code | 0x80,
            data: vec![error_code],
            is_error: true,
        }
    }

    // ── private ──────────────────────────────────────────────────────────────

    fn dispatch(&self, frame: &ModbusFrame) -> DispatchResult {
        for handler in &self.handlers {
            if handler.supports(frame.function_code) {
                return handler.handle(frame);
            }
        }
        DispatchResult::Unhandled
    }
}

